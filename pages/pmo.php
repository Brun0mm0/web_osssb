            <div id="plan-materno-infantil">
                <h4>Plan Materno Infantil (PMI)</h4>
            </div>
            <div>
                <p>Este plan abarca la cobertura del embarazo y del parto a partir del momento del diagnóstico y hasta el primer mes luego del nacimiento y la atención del recién nacido hasta cumplir el año de edad, tanto en internación como en ambulatorio y exceptuado del pago de todo tipo de coseguros para las atenciones y medicaciones específicas.</p>
                <br>
                <ul class="pmo-listas pmo-single">
                    <li>
                        <p>Cobertura del 100% de la atención integral del embarazo, parto y del recién nacido.</p>
                    </li>
                    <li>
                        <p>Cobertura del 100% en medicamentos inherentesal estado de embarazo, parto y puerperio para la madre y del 40% para medicamentos ambulatorios no relacionados.</p>
                    </li>
                    <li>
                        <p>Cobertura 100% en medicamentos para el niño hasta el año de vida.</p>
                    </li>
                </ul>
            </div>
            <div id="antecion-embarazo-parto">
                <h4>Atención de Embarazo y Parto</h4>
            </div>
            <div>
                <ul class="pmo-listas pmo-single">
                    <li>
                        <p>Consultas: En embarazos normales se realizará un control mensual hasta la semana 35, quincenal de la 35 a la 38 y semanal desde la 38 hasta el parto. En los mismos se determinará edad gestacional y fecha probable de parto, efectuándose:</p>
                        <ul class="pmo-square">
                            <li>
                                <p>Estudios exclusivamente relacionados con el embarazo, el parto y el puerperio.</p>
                            </li>
                        </ul>
                    </li>
                </ul>
                <br>
                <p>En embarazos de riesgo, se deberán contemplar controles sin restricciones, con la cobertura de los estudios que cada patología requiera.
                    Internación: Será como mínimo de 48 hs. para un parto por vía vaginal y de 72 hs. para una cesárea.</p>
                <p>En madres Rh (-) no sensibilizadas con hijos (+) se dará cobertura con gammaglobulina anti Rh. Dentro de las 72 hs. de ocurrido el parto y durante la internación.</p>
                <p>En madres Rh (-) cuyos embarazos terminan en abortos se dará cobertura con gammaglobulina anti Rh.</p>
                <p>Consejos para lactancia materna, puericultura y procreación responsable.</p>
            </div>
            <div id="atencio-recien-nacido">
                <h4>Atención del Recién Nacido y hasta el Año de Vida</h4>
            </div>
            <div>
                <p>Brindar cobertura de internación de 48 hs. con control adecuado por profesional a todos los recién nacidos.</p>
                <p>En caso de recién nacidos patológicos brindar cobertura de internación, medicamentos y métodos diagnósticos sin límite de tiempo.</p>
                <br>
                <p>Estudios para detección de fenilcetonuria, hipotiroidismo y enfermedad fibroquística en el recién nacido. (Ley 26279).</p>
                <br>
                <p>Consultas de seguimiento y control e inmunizaciones del período, incluyendo la vacunación contra el virus de la hepatitis B.</p>
                <br>
                <p>Con el objeto de promover la lactancia materna no se cubrirán leches maternizadas o de otro tipo, salvo expresa indicación médica, con evaluación de la Auditoría médica.</p>
            </div>
            <div id="prevencion-cancer-cuello-uterino">
                <h4>Programa de Prevención de Cáncer de Cuello Uterino</h4>
            </div>
            <div>
                <p>Se debe realizar diagnóstico y tratamiento de todas las afecciones malignas del cuello uterino.</p>
                <p>La citología (Papanicolau - PAP) debe realizarse en forma anual y luego de dos resultados normales, cada tres años a toda mujer desde los 18 años o menos que haya iniciado relaciones sexuales, manteniendo esta periodicidad hasta los 70 años. Después de los 70 años se efectuará cada cinco años.</p>
                <p>En caso que el PAP de control fuera anormal se debe realizar colposcopía y cepillado endocervical. Si estos controles fueran normales, el PAP se debe repetir dos veces en un año y si estos fueran anormales continúa con un PAP anual por tres años y de persistir normales vuelve al control cada 3 años.</p>
                <p>De registrarse colposcopía patológica o anormal, se debe realizar biopsia dirigida colposcópica, si esta verificara patología pasa al tratamiento de la misma.</p>
                <p>De ser la biopsia negativa se realizará un control anual con PAP y colposcopía. Si estos estudios fueran normales continúa con el control trianual.</p>
            </div>
            <div id="prevencion-cancer-mama">
                <h4>Programa de Prevención del Cáncer de Mama</h4>
            </div>
            <div>
                <p><b>Población en riesgo estándar</b></p>
                <p>Se le debe realizar una mamografía a los 45 años.</p>
                <p>Si fuera negativa se debe continuar con una mamografía cada 2 años hasta los 70 años. Con resultado mamográfico anormal se realizará biopsia. En caso de resultado positivo pasa a tratamiento. Si en cambio fuera negativa, realizar una mamografía al año y con resultado normal regresa a controles bianuales.</p>
                <br>
                <p><b>Población en riesgo aumentado</b></p>
                <p>Las mujeres por debajo de 40 años, con una fuerte historia familiar de cáncer de mama, deben entrar en planes diferenciados de prevención. En esta situación se recomienda una mamografía a los 35 años y una mamografía anual a partir de los 40 años.</p>
                <br>
                <p>Se excluyen de la cobertura todo tipo de tratamientos y/o protocolos de carácter experimental o en fase de prueba.</p>
